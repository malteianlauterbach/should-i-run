// URL of the webpage to scrape
$url = "http://travel.state.gov/content/travel/en/traveladvisories/traveladvisories.html";

function fetch_content($url) {
    $options = [
        "http" => [
            "method" => "GET",
            "header" => "User-Agent: PHP"
        ],
        "ssl" => [
            "verify_peer" => false,
            "verify_peer_name" => false
        ]
    ];
    $context = stream_context_create($options);
    
    $html = @file_get_contents($url, false, $context);
    if ($html === FALSE) {
        $error = error_get_last();
        echo("Failed to fetch the webpage content: " . $error['message']);
        throw new Exception("Failed to fetch the webpage content. Error: " . $error['message']);
    }
    echo("Webpage content fetched successfully.");
    return $html;
}

function parse_travel_advisories($html, $country_codes) {
    $advisories = [];
    $dom = new DOMDocument();
    @$dom->loadHTML($html);
    $xpath = new DOMXPath($dom);
    $rows = $xpath->query("//tr");

    if ($rows->length === 0) {
        echo "No advisory rows found in the webpage.\n";
        return $advisories;
    }

    foreach ($rows as $row) {
        $country_name_node = $row->getElementsByTagName('a')->item(0);
        if (!$country_name_node) {
            continue;
        }
        $country_name = str_replace(" Travel Advisory", "", $country_name_node->textContent);
        $advisory_level = $row->getElementsByTagName('td')->item(1)->textContent ?? '';
        $date = $row->getElementsByTagName('td')->item(2)->textContent ?? '';

        $country_name_trimmed = trim(str_replace('\'s', 's', $country_name));
        $country_code = array_search($country_name_trimmed, $country_codes);

        if ($country_code === false) {
            echo("Warning: Country name '$country_name_trimmed' could not be matched to a country code.");
            $country_code = $country_name_trimmed;
        }

		$level = trim(str_replace("Level ", "", explode(":", $advisory_level)[0]));
		if ($level === 'Other') {
			$level = '4';
		}

        $advisories[] = [
            "CountryCode" => $country_code,
            "Advisory" => $level,
            "Date" => $date
        ];
    }

    echo(count($advisories) . " advisories parsed successfully.");
    return $advisories;
}

function generate_xml($advisories, $output_file) {
    $xml = new SimpleXMLElement("<TravelAdvisories></TravelAdvisories>");

    foreach ($advisories as $advisory) {
        if (empty($advisory["Advisory"]) || empty($advisory["Date"])) {
            continue;
        }
        $entry = $xml->addChild("Advisory");
        $entry->addChild("CountryCode", $advisory["CountryCode"]);
        $entry->addChild("Level", $advisory["Advisory"]);
        $entry->addChild("Date", $advisory["Date"]);
    }

    if ($xml->asXML($output_file)) {
        echo("XML file generated successfully at " . $output_file);
    } else {
        echo("Failed to generate the XML file.");
    }
}

