    // Get today's and yesterday's dates
    $today = new DateTime();
    $yesterday = (clone $today)->modify('-1 day');

    // Initialize an array to hold matched advisories
    $recent_advisories = [];

    // Check each advisory
    foreach ($xml->Advisory as $advisory) {
        $country_code = (string) $advisory->CountryCode;
        $level = (string) $advisory->Level;
        $date = DateTime::createFromFormat('F j, Y', (string) $advisory->Date);

        if ($date && ($date->format('Y-m-d') === $today->format('Y-m-d') || $date->format('Y-m-d') === $yesterday->format('Y-m-d'))) {
            $country_name = $country_codes[$country_code] ?? 'Unknown Country';
            $recent_advisories[] = [
                'CountryName' => $country_name,
                'UpdateDate' => $date->format('F j, Y'),
                'Level' => $level
            ];
        }
    }

    // Output the results
    if (count($recent_advisories) > 0) {
        echo "<table>";
        echo "<tr><th>Country</th><th>Update Date</th><th>Warning Level</th></tr>";

